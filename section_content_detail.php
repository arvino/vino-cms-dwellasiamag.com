<div class="body">
	<p>At his workshop in the East Bay, he works with a fleet of California artisans—platers, refinishers, caners, upholsterers, framers, lacquerers—to impeccably restore the pieces he sells both online and in his shop. Dedicated to the credo of “less is more,” he recently got rid of his cell phone even though he knows that might hurt sales. (“Why are 4,000 conversations a day better than one?” he asks.) And in his store, he prods shoppers to think before they buy. “I’ve told customers, ‘Maybe you don’t need another chair,’” Houston says. “Even though I sell things, I do like to remind people that you don’t need to own it to appreciate it.”</p>	<div class="questionsAnswers">
								<div class="mediaContainer photoFunctions position2"><img src="http://media.dwell.com/images/230*344/Houston-Shop.jpg" alt="HoustonShop."></div><div class="caption position2 width1"><p>The shop is stocked with an array of design books, pendant lamps, and mid-century teak furniture.</p></div>
				
		<p class="question"><q><strong>What draws you to vintage?</strong></q></p>
		
					<blockquote class="answer"><p>I love old stuff. It has a vibe. Also, vintage is an incredible value. If you put my stuff next to brand-new Knoll or Cappellini, it’s cheap! Well, “cheap” is a derogatory word, but it’s imminently affordable.</p></blockquote>
											
		<p class="question"><q><strong>What’s best about your job?</strong></q></p>
		
					<blockquote class="answer"><p>I really enjoy fixing things. Also, I complain as much as the next guy about retail and the state of the public mind-set, but, you know, I meet  really interesting people pretty often. I am a magnet for people who are interested in art and good design.</p></blockquote>
											
		<p class="question"><q><strong>How do you define “good design”?</strong></q></p>
		
					<blockquote class="answer"><p>Good design is fixable. It’s not fixable if you don’t care about it­—it’s not fixable if it was trendy or made with poor materials. And good design carries a higher price tag: Someone was paid a living wage to make it; it was made with materials of which there is not an unlimited supply; and it was assembled using more precise tools.</p></blockquote>
											
		<p class="question"><q><strong>Do you consider restoring furniture a form of recycling?</strong></q></p>
		
					<blockquote class="answer"><p>No! To me, recycling is part of the problem. It’s actually downcycling— a degradation of material—and it takes a lot of energy to do it. The worst thing about recycling is that it makes people think they don’t have to change their habits. I don’t do recycling. I reuse, and I restore, and I repair.</p></blockquote>
												<div class="mediaContainer photoFunctions position1"><img src="http://media.dwell.com/images/230*344/Houston-Shop-Detail.jpg" alt="HoustonShopDetail.j"></div><div class="caption position1 width1"><p>The steel candleholder is a Kubus 8, by Mogens Lassen.</p></div>
				
		<p class="question"><q><strong>What differentiates Modern Artifacts from other design stores?</strong></q></p>
		
					<blockquote class="answer"><p>I don’t push people to buy things. I’m a terrible salesman. I’ll be talking about NAFTA when I’m supposed to be selling an Eames chair. And if there’s something in the store that’s not perfect, or not quite what it could be, I’ll tell you right then and there: You’re not going to be able to fix it, because I already tried.</p></blockquote>
											
		<p class="question"><q><strong>What’s your favorite object in your store right now?</strong></q></p>
		
					<blockquote class="answer"><p>I’m a little bored with modern, to be honest with you. So my favorite thing at the moment is this asymmetrical Paolo Deganello Torso chair from 1982. Some people just think it’s loud and crazy, but it’s actually very sittable, very beautiful, and it’s flamboyant without being stupid. And it’s from the ‘80s, which was my period.</p></blockquote>
												<div class="mediaContainer photoFunctions position2"><img src="http://media.dwell.com/images/230*350/Houston-Shop-Long-View.jpg" alt="HoustonShopLongView.jp"></div><div class="caption position2 width1"><p>A long view of the appealingly cluttered shop, with a Noguchi paper lamp and a pair of wool-upholstered teak chairs in the foreground.</p></div>
				
		<p class="question"><q><strong>What’s the best thing about owning a shop?</strong></q></p>
		
					<blockquote class="answer"><p>I love getting excited about something I thought I didn’t like. For example, I usually hate Panton; his stuff is loud and garish. But I got his lamps in, and being with them for a little while forced me to realize that, Wow, these are actually pretty good. I like being surprised and forced to flex.</p></blockquote>
											
		<p class="question"><q><strong>Any advice for would-be customers?</strong></q></p>
		
					<blockquote class="answer"><p>I suspect most people are like me: They have tons of shit they don’t need. Get rid of it all! Just keep the things you really like. And then that helps me. Because that’s what I sell: the stuff that costs more, that you have to think hard about, and that you’re not going to get rid of. And if you do clean house, you might actually make room for something from me. That’s the hope.</p></blockquote>
