		<h1>Consumer Retorts</h1>
		
	</div>
		<div class="summary">
		<p>Chris Houston, the charmingly curmudgeonly owner of Modern Artifacts in San Francisco, is not your typical retailer. Though his shop is packed to the rafters with an eclectic and highly covetable range of vintage furniture, lighting, art, and craft, Houston takes a slow and thoughtful approach to retail and commerce.</p>
	</div>


